/// shape × gender → 21-point quantile. [FaceShape.unknown] 이거나 key 누락 시
/// shape-agnostic fallback.
List<double> _quantileFor(Attribute attr, Gender gender, FaceShape shape) {
  final byGender = _attrQuantilesByShape[shape];
  if (byGender != null) {
    final byAttr = byGender[gender];
    if (byAttr != null) {
      final q = byAttr[attr];
      if (q != null) return q;
    }
  }
  return gender == Gender.male
      ? _attrQuantilesMale[attr]!
      : _attrQuantilesFemale[attr]!;
}

/// raw → empirical percentile (0..1) by linear interp between quantile points.
double _rawToPercentile(
    double raw, Attribute attr, Gender gender, FaceShape shape) {
  final q = _quantileFor(attr, gender, shape);
  if (raw <= q[0]) return 0.0;
  if (raw >= q[20]) return 1.0;
  for (int i = 0; i < 20; i++) {
    final lo = q[i];
    final hi = q[i + 1];
    if (raw <= hi) {
      final span = hi - lo;
      final t = span > 0 ? (raw - lo) / span : 0.0;
      return (i + t) / 20.0;
    }
  }
  return 1.0;
}

/// Blend: 35% within-face rank (spread) + 65% global percentile.
///
/// 2026-04-19 v2.7: MC sampler bias 를 0.0 → 0.2 로 맞추면서 quantile CDF 가
/// realistic 분포와 일치. rank 비중은 spread invariant 보장 위해 보유.
const double _rankWeight = 0.35;
const double _globalWeight = 0.65;

/// Normalizes 10 attribute raw scores into 5.0–10.0. Guarantees:
///   - top attribute ≥ 8.0
///   - bottom attribute ≤ 7.0
///   - spread between top and bottom ≥ 3.0
///
/// [shape] 는 per-shape quantile lookup 용. [FaceShape.unknown] 일 때는
/// shape-agnostic 테이블로 fallback.
Map<Attribute, double> normalizeAllScores(
  Map<Attribute, double> rawScores,
  Gender gender, {
  FaceShape shape = FaceShape.unknown,
}) {
  final globalPct = <Attribute, double>{};
  for (final entry in rawScores.entries) {
    globalPct[entry.key] =
        _rawToPercentile(entry.value, entry.key, gender, shape);
  }

  // Rank by global percentile so all attributes share a 0..1 scale
  // regardless of raw-score magnitudes.
  final sorted = globalPct.entries.toList()
    ..sort((a, b) => b.value.compareTo(a.value));
  final ranks = <Attribute, int>{};
  for (int i = 0; i < sorted.length; i++) {
    ranks[sorted[i].key] = i;
  }
  final denom = (sorted.length - 1).toDouble();

  final result = <Attribute, double>{};
  for (final attr in Attribute.values) {
    final rankPct = (denom - ranks[attr]!) / denom;
    final gPct = globalPct[attr]!;
    final blended = _rankWeight * rankPct + _globalWeight * gPct;
    final score = 5.0 + blended * 5.0;
    result[attr] = (score * 10).round() / 10.0;
  }
  return result;
}

/// Single-attribute normalization — used by partial flows
/// that can't run the full 10-attribute rank step.
double normalizeScore(
  double raw,
  Attribute attr,
  Gender gender, {
  FaceShape shape = FaceShape.unknown,
}) {
  final pct = _rawToPercentile(raw, attr, gender, shape);
  final score = 5.0 + pct * 5.0;
  return (score * 10).round() / 10.0;
}
