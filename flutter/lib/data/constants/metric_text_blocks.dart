/// 33 metric × 3-band 관상학 해석 + combo rule SSOT.
///
/// UI 의 `_NodeBar._buildBody()` 가 per-metric row 아래에 z-band 본문을 노출하고,
/// 같은 노드 내 metric 쌍의 combo 해석을 추가 렌더한다.
///
/// Band 경계 (node_text_blocks 보다 촘촘):
///   z ≥ +0.5  → high
///   |z| < 0.5 → mid
///   z ≤ -0.5  → low
///
/// 문체: node_text_blocks.dart 와 동일 — 한글 관상 고전 어휘, 2~3 문장, 남녀 공용.
/// mid band 는 평균권 중립 해석 한 문장.
library;

// ────────────────────────── MetricTextBlock ──────────────────────────

class MetricTextBlock {
  final String? high; // z >= 0.5
  final String? mid; // |z| < 0.5
  final String? low; // z <= -0.5
  const MetricTextBlock({this.high, this.mid, this.low});
}

/// z-score → band 본문 선택.
String? metricBodyForZ(String metricId, double z) {
  final block = metricTextBlocks[metricId];
  if (block == null) return null;
  if (z >= 0.5) return block.high;
  if (z <= -0.5) return block.low;
  return block.mid;
}

