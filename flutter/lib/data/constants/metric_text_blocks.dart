/// 현재 metric z-score map 에서 trigger 된 combo 목록.
/// [nodeMetricIds] 로 현재 노드 scope 내 metric 만 필터.
List<MetricComboRule> triggeredCombos(
  List<String> nodeMetricIds,
  Map<String, double> zByMetricId,
) {
  final idSet = nodeMetricIds.toSet();
  final result = <MetricComboRule>[];
  for (final rule in metricComboRules) {
    if (!idSet.contains(rule.a) || !idSet.contains(rule.b)) continue;
    final za = zByMetricId[rule.a];
    final zb = zByMetricId[rule.b];
    if (za == null || zb == null) continue;
    final aMatch = rule.aHigh ? za >= 0.5 : za <= -0.5;
    final bMatch = rule.bHigh ? zb >= 0.5 : zb <= -0.5;
    if (aMatch && bMatch) result.add(rule);
  }
  return result;
}
