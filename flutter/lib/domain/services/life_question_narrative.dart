      '이는 잔병을 잘 겪지 않는다는 뜻이 아니라, 결정적 위기가 찾아와도 회복의 탄성이 남들보다 한 단계 강하다는 의미이며, '
      '수명의 길이보다 삶의 밀도 측면에서 훨씬 더 큰 강점을 발휘하는 구조입니다. ',
    );
  } else if (_band(stab) == _Band.mid) {
    buf.write(
      '당신의 건강운은 중간에서 단단한 결을 유지하는 유형입니다. '
      '관상학에서 \'질액궁\'과 턱의 균형이 극단적이지 않고 안정적으로 자리한 상으로, 치명적 기울어짐은 없지만 '
