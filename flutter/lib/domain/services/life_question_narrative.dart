String buildWealthNarrative(FaceReadingReport r) {
  final wealth = _attr(r, Attribute.wealth);
  final buf = StringBuffer();

  // 장점 / 타입 진단
  if (_band(wealth) == _Band.high) {
    buf.write(
      '당신의 얼굴에는 재물의 기운이 또렷하게 서려 있습니다. '
      '관상학에서 \'재백궁(財帛宮)\'이라 부르는 코와 광대의 조화가 충실한 상으로, '
      '돈이 당신에게 머물고 싶어하는 구조가 골상 안에 이미 새겨져 있습니다. '
      '이는 단순히 \'돈을 잘 버는 사람\'이라는 뜻이 아니라, 수입과 지출 사이에서 \'남기는 감각\'이 직관적으로 작동하는 기질을 의미하며, '
      '같은 기회를 만나도 다른 이보다 반 걸음 먼저 자리를 잡고, 같은 손실을 겪어도 바닥을 확인하는 타이밍이 빠른 편입니다. ',
    );
  } else if (_band(wealth) == _Band.mid) {
    buf.write(
      '당신의 재물운은 극단적이지 않은 대신 단단한 결을 갖추고 있습니다. '
      '관상학에서 \'재백궁\'이 거칠지 않고 순하게 열린 상으로, 한 방에 큰 부를 잡는 기질보다는 '
      '꾸준히 쌓아 올리는 누적형 재물 구조에 해당합니다. '
      '이는 위험을 감수하는 기개는 상대적으로 적지만 잃지 않는 힘이 강한 유형이며, '
      '근로소득·자영업·장기 투자처럼 시간을 내 편으로 돌려세우는 영역에서 강점을 발휘합니다. ',
    );
  } else {
    buf.write(
      '당신의 재물운은 현재 구조로만 보면 폭발적이지 않은 유형에 속합니다. '
      '관상학에서 \'재백궁\'의 자리가 가볍거나 골격보다 살집이 받쳐주지 못하는 상에 해당하며, '
      '돈의 흐름이 당신 손을 오래 머무르지 않고 빠르게 흘러나가는 기질을 암시합니다. '
      '다만 재물운은 관상학 안에서도 가장 후천적 수양으로 바뀌는 영역으로 알려져 있어, '
      '기질의 한계를 시스템으로 보완하는 순간 전혀 다른 곡선이 그려지기 시작합니다. ',
    );
  }

  // 세부 보강
  if (_fired(r, 'P-06') || _nodeOwnZ(r, 'nose') >= 1.0) {
    buf.write(
      '코(準頭)가 듬직하게 자리잡은 구조는 \'중년발복(中年發福)\'이라 하여 30대 후반에서 40대 사이에 재물의 꼭짓점이 오는 기질을 암시합니다. ',
    );
  }
  if (_fired(r, 'O-CK') || _nodeOwnZ(r, 'cheekbone') >= 0.8) {
    buf.write(
      '광대의 태산·화산이 힘차게 받쳐주는 구조는 사람을 부려 돈을 만드는 기질을 뜻하며, 순수 근로보다 운영·관리·조직으로 부를 키우는 길이 더 맞습니다. ',
    );
  }
  if (_fired(r, 'Z-04') || _nodeOwnZ(r, 'chin') >= 1.0) {
    buf.write(
      '턱(地閣)이 두터운 구조는 \'말년재운\'을 상징하며, 50대 이후에도 재물의 뿌리가 마르지 않고 오히려 더 깊어지는 후복(後福)의 기질을 담고 있습니다. ',
    );
  }
  buf.write('\n\n');

  // 단점
  if (_band(wealth) == _Band.low) {
    buf.write(
