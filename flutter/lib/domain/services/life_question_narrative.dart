String buildConclusionNarrative(FaceReadingReport r) {
  final top = r.archetype.primaryLabel;
  final sub = r.archetype.secondaryLabel;
  final isMale = _isMale(r);
  final buf = StringBuffer();

  // 통합 진단
  buf.write(
    '지금까지 읽어온 여러 영역을 한 장으로 겹쳐보면, 당신의 관상은 \'$top\'의 골격 위에 \'$sub\'의 결이 함께 흐르는 구조입니다. '
    '관상학에서 이 두 기운이 한 얼굴에 실린다는 것은, 단일한 방향으로 힘을 쏟는 전형보다 상황에 따라 두 얼굴을 번갈아 쓸 수 있는 ${isMale ? '장부(丈夫)' : '여인(女人)'}의 관상에 해당한다는 의미입니다. '
    '겉으로 가장 먼저 드러나는 것은 \'$top\'의 기운이지만, 당신의 인생 중반을 실질적으로 움직이는 동력은 오히려 \'$sub\'의 결에 더 많이 담겨 있습니다. ',
  );

  final specialArchetype = r.archetype.specialArchetype;
  if (specialArchetype != null) {
    buf.write(
      '특히 당신의 얼굴에는 \'$specialArchetype\'이 동시에 서려 있어, '
      '평균적 해석의 범위를 넘어서는 결정적 국면을 인생의 중·후반에 한 번 이상 통과하게 될 가능성이 높습니다. '
