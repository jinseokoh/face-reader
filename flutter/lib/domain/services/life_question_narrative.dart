      '이는 잘 관리하면 평균 이상, 방치하면 평균 이하로 떨어지는 양면성을 가진 유형이며, '
      '결국 30대부터 들이는 건강 자산의 총량이 말년의 곡선을 결정합니다. ',
    );
  } else {
    buf.write(
      '당신의 건강운은 관상학적으로 세심한 관리가 전제될 때 가장 크게 길어지는 구조입니다. '
      '\'질액궁\'이 약하거나 턱의 받침이 가볍게 드러나는 상은 체질의 저점이 남들보다 낮은 구간에 자주 들어간다는 뜻이며, '
      '이는 \'약하다\'가 아니라 \'예민하다\'에 가깝습니다. '
      '예민한 몸은 신호를 일찍 보내주기에, 오히려 그 신호를 잘 읽고 관리하면 둔감한 사람보다 훨씬 오래 건강을 유지할 수 있는 역설적 유형입니다. ',
    );
  }

  if (_fired(r, 'P-07') || _nodeAbsZ(r, 'nose') >= 1.2) {
    buf.write(
      '코의 구조가 특징적으로 드러나는 상은 관상학에서 40대 전후의 \'중년 건강 관문\'을 강조하는 신호로 읽히며, 호흡기·순환기 쪽을 미리 점검해 두는 것이 큰 도움이 됩니다. ',
    );
  }
  if (_fired(r, 'Z-09')) {
    buf.write(
      '상정의 기운이 강한 상은 머리를 너무 많이 쓰는 기질을 의미하며, 수면의 질이 전체 건강의 다른 어떤 요소보다 먼저 흔들리기 쉬운 유형입니다. ',
    );
  }
  if (_fired(r, 'O-CH') || _nodeOwnZ(r, 'chin') >= 0.8) {
    buf.write(
      '턱이 듬직한 구조는 관상학에서 \'말년 강건\'의 상징으로, 50대 이후의 체력이 오히려 동년배보다 떨어지지 않는 기질을 뒷받침합니다. ',
    );
  }
  buf.write('\n\n');

  // 단점
  if (_band(stab) == _Band.low) {
    buf.write(
      '다만 직시해야 할 지점이 있습니다. '
      '당신의 구조는 과로와 감정적 소모에 특히 취약한 편이며, 일상의 작은 신호를 무시하고 밀어붙이는 시기가 길어지면 특정 장기에 부담이 집중적으로 쌓이는 패턴이 반복되기 쉽습니다. '
      '관상학에서 \'질액궁\'이 예민한 상은 \'남들이 버티는 강도를 동일하게 버티려 하면 안 된다\'는 경고의 의미를 담고 있으며, '
      '당신에게 맞는 리듬을 찾는 것이 수명의 길이를 결정짓는 가장 큰 변수가 됩니다. ',
    );
  } else {
    buf.write(
      '다만 당신은 \'건강에 자신이 있다\'는 그 자신감이 가장 큰 위험 요인이 될 수 있는 유형입니다. '
      '타고난 기본기가 좋기 때문에 몸의 경고 신호를 쉽게 묵살하고 밀어붙이다가 어느 시점에 한 번에 무너지는 패턴이 종종 나타납니다. '
      '관상학에서 강건한 상의 전형적 그림자로, 조기 검진과 정기적 관리를 오히려 더 꼼꼼히 해야 타고난 수명의 상한까지 안전하게 갈 수 있습니다. ',
    );
  }
  buf.write('\n\n');

  // 조언
  buf.write(
    '건강을 지키는 길은 세 가지입니다. '
    '첫째, 수면·식사·운동 중 당신에게 가장 약한 한 가지를 먼저 표준화하십시오. 세 가지를 동시에 잡으려는 시도는 결국 어느 하나도 자리 잡지 못하는 결과를 가져옵니다. '
    '둘째, 몸이 보내는 \'이상 없음\'의 신호를 맹신하지 마십시오. 관상학이 말하는 건강운은 \'자각 증상이 없을 때 미리 점검하는 습관\'이 있을 때에만 상한선에 도달합니다. '
    '셋째, 감정의 피로가 몸의 피로로 옮겨 가는 통로를 스스로 알아두십시오. 당신의 관상에서 수명을 가장 많이 갉아먹는 요인은 육체의 과로가 아니라 해소되지 않은 감정의 누적인 경우가 많습니다.',
  );

  return buf.toString();
