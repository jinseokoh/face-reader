String buildTalentNarrative(FaceReadingReport r) {
  final intel = _attr(r, Attribute.intelligence);
  final lead = _attr(r, Attribute.leadership);
  final emo = _attr(r, Attribute.emotionality);
  final attrv = _attr(r, Attribute.attractiveness);
  final social = _attr(r, Attribute.sociability);

  final buf = StringBuffer();

  // ─── 장점: 타입별 재능 진단 ───
  if (_band(intel) == _Band.high && _band(lead) == _Band.high) {
    buf.write(
      '당신의 얼굴에는 지략과 통솔이 한 몸에 깃들어 있습니다. '
      '관상학에서 \'명궁(命宮)\'이라 부르는 미간의 열림과 이마의 광활함이 함께 드러나는 상으로, '
      '머리로 꿰뚫어 보는 힘과 앞장서 밀어붙이는 힘이 한 얼굴에 공존하는 드문 구조입니다. '
      '이는 문관(文官)과 무관(武官)의 경계를 넘나드는 기질로, '
      '혼자 사유하는 시간에서 돌파구를 얻되 사람 앞에 설 때 비로소 완성되는 이중 동력이 당신 재능의 중심축입니다. ',
    );
  } else if (_band(intel) == _Band.high) {
    buf.write(
      '당신이 타고난 가장 뚜렷한 재능은 \'읽는 힘\'입니다. '
      '관상학에서 이마의 천정(天庭)과 미간의 인당(印堂)이 맑게 열린 상으로, '
      '표면에 드러난 정보를 넘어 그 뒤의 맥락과 의도를 읽어내는 통찰이 깊게 자리잡았습니다. '
      '남들이 사태가 지난 뒤 비로소 깨닫는 일을 당신은 사건의 한가운데서 이미 흐름을 짚어내며, '
      '그 차분한 예측력은 공부·기획·분석 어느 길로 가든 당신의 가장 믿을 만한 무기가 됩니다. ',
    );
  } else if (_band(lead) == _Band.high) {
    buf.write(
      '당신의 타고난 재능은 사람을 \'움직이게 하는 힘\'입니다. '
      '관상학에서 \'관록궁(官祿宮)\'과 광대의 태산·화산이 묵직하게 자리한 상으로, '
      '말하지 않아도 주변이 당신의 결정을 기다리게 되는 무형의 장악력이 얼굴에 실려 있습니다. '
      '이 기운은 회의실의 침묵을 깨는 결단으로, 혹은 망설이는 팀을 한 방향으로 정렬시키는 호령으로 발현되며, '
      '위기 상황에서 오히려 더 또렷하게 드러나는 대장부의 기개가 당신 재능의 본체입니다. ',
    );
  } else if (_band(emo) == _Band.high) {
    buf.write(
      '당신의 재능은 감수성의 깊이에서 발화합니다. '
      '관상학에서 눈매에 수기(水氣)가 흐르고 입매의 표현이 풍부한 상으로, '
      '남이 무심코 흘리는 기색과 공기의 미세한 온도 변화를 당신은 피부로 읽어냅니다. '
      '이 감지력은 예술적 창조·상담·디자인처럼 \'사람의 마음을 옮겨 담는\' 일에서 가장 극적으로 빛나며, '
      '머리로만 굴리는 사람이 절대 흉내 낼 수 없는 결의 섬세함이 당신 재능의 진짜 자산입니다. ',
    );
  } else if (_band(attrv) == _Band.high) {
    buf.write(
      '당신의 재능은 존재감 그 자체에 있습니다. '
      '관상학에서 오관(五官)의 균형이 수려하고 골격의 비례가 이상적인 상으로, '
      '같은 공간에 있을 때 자연스럽게 시선의 무게중심이 당신 쪽으로 기울어지는 기질이 타고났습니다. '
      '이는 단순한 외모의 문제가 아니라, 말투·자세·표정의 전체 흐름이 한 줄기로 정렬되어 있을 때 나오는 일관된 매력이며, '
      '사람 앞에 서는 모든 직역에서 당신은 특별한 지렛대를 하나 더 가지고 시작합니다. ',
    );
  } else if (_band(social) == _Band.high) {
    buf.write(
      '당신의 재능은 관계를 엮어내는 손끝에 있습니다. '
      '관상학에서 \'천이궁(遷移宮)\'이 부드럽게 열리고 입매가 온화한 상으로, '
      '이질적인 사람들 사이에 자연스러운 다리를 놓는 감각이 몸에 배어 있습니다. '
      '남을 설득하려 애쓰지 않고도 사람들이 당신 곁에서 무장해제되는 편안함이 최대의 강점이며, '
      '이 기질은 영업·중개·상담·네트워크 비즈니스처럼 \'사람 자체가 자산\'인 영역에서 특히 크게 확장됩니다. ',
    );
  } else {
    buf.write(
      '당신의 재능은 한 방향으로 편중되어 있지 않고 여러 영역에 고루 잠재된 형태입니다. '
      '관상학에서 삼정(三停)이 균형을 이루고 어느 한 부위가 극단적으로 치우치지 않은 상으로, '
      '이는 \'무색(無色)이 곧 다색(多色)\'이라 불리는 유형에 해당합니다. '
      '겉보기엔 평범해 보이지만, 어떤 환경에 들어가도 그 자리의 언어를 빠르게 흡수해 맞춰가는 적응력이 바로 당신의 재능이며, '
      '오래 지켜본 사람일수록 당신의 진가를 알아보게 되는 늦게 피는 꽃의 기질입니다. ',
    );
  }

  // ─── 장점 보강: 특정 rule / 노드 기반 세부 문장 ───
  if (_fired(r, 'O-EB1') || _fired(r, 'O-EB2')) {
    buf.write(
      '눈썹이 또렷하게 자리잡은 당신은 \'보수관(保壽官)\'이 강한 상으로, '
      '새로운 지식을 익히는 초기 속도가 또래보다 한 발 빠르고 의지가 중간에 꺾이지 않는 집요함까지 갖추었습니다. ',
    );
  }
  if (_fired(r, 'P-02') || _nodeOwnZ(r, 'forehead') >= 1.0) {
    buf.write(
      '넓고 반듯한 이마는 \'천정(天庭) 열림\'이라 하여 윗사람의 도움과 윗선의 기회가 먼저 찾아오는 기질을 암시합니다. ',
    );
  }
  if (_fired(r, 'O-EM')) {
    buf.write(
      '눈과 입의 표현력이 함께 살아 있는 구조는 감정을 언어로 정확히 옮기는 드문 재능으로, 글쓰기·강연·연기에서 남다른 설득력을 만들어냅니다. ',
    );
  }
  buf.write('\n\n');

  // ─── 단점: 하위 attribute / 구조적 약점 ───
  final stability = _attr(r, Attribute.stability);
  final trust = _attr(r, Attribute.trustworthiness);
  if (_band(stability) == _Band.low || _fired(r, 'Z-09')) {
    buf.write(
      '다만 재능의 빛이 강한 만큼 그림자도 분명합니다. '
      '당신은 흥미가 옮겨 붙는 속도가 빠른 대신, 한 우물을 오래 파는 안정된 뿌리 내림이 상대적으로 약한 편입니다. '
      '관상학에서는 이를 \'상정(上停) 과강(過强)\'이라 부르는데, 머리가 먼저 달려 나가 몸이 따라잡지 못하는 상태를 뜻하며, '
      '관심사가 너무 자주 이동하면 재능이 결정적 순간에 \'쌓이지\' 못하고 \'흩어진\' 채로 흘러가버리기 쉽습니다. ',
    );
  } else if (_band(trust) == _Band.low || _fired(r, 'O-DC2')) {
    buf.write(
      '다만 당신의 재능은 안으로 파고드는 성질이 강해, 자신이 잘하는 것을 밖으로 내보이는 일에는 상대적으로 서툰 편입니다. '
      '관상학에서 \'출납관(出納官)\'이 닫혀 있는 상에 해당하는 기질로, 속에 쌓아둔 것이 많은데도 표현의 통로가 좁아 저평가되는 경우가 종종 발생합니다. '
      '실력은 충분히 쌓였는데 \'홍보가 약해 밀리는\' 상황이 반복된다면 이는 기질의 문제지 능력의 문제가 아닙니다. ',
    );
  } else if (_band(emo) == _Band.high && _band(stability) != _Band.high) {
    buf.write(
      '다만 당신의 감수성은 양날의 칼입니다. '
      '세상을 섬세하게 읽는 감각이 반대로 작동할 때는 작은 비판에도 기운이 크게 출렁이고, 주변 분위기에 너무 쉽게 동화되어 자기 중심을 잠깐 놓쳐버리는 일이 생깁니다. '
      '관상학에서 \'명궁\'이 맑은 만큼 탁한 기운이 들어올 때도 그대로 비치는 구조이기 때문이며, 재능의 깊이와 상처받기 쉬운 연약함이 같은 뿌리에서 나옵니다. ',
    );
  } else {
    buf.write(
      '다만 당신의 재능은 한 번에 폭발적으로 터지기보다 쌓는 시간에 정직하게 비례합니다. '
      '관상학에서 \'대기만성(大器晩成)\'이라 부르는 기질로, 조급한 마음으로 빠른 결실을 기대하면 오히려 제풀에 지쳐버리기 쉬운 구조입니다. '
      '남들이 일찍 빛나는 모습에 흔들려 자기 페이스를 놓치면 타고난 잠재력이 절반밖에 열리지 않는 유형이라는 점을 자각할 필요가 있습니다. ',
    );
  }
  buf.write('\n\n');

  // ─── 조언 ───
  buf.write(
    '재능을 살리는 길은 분명합니다. '
    '첫째, 당신의 강점 영역을 최소 3년 이상 한 줄기로 밀어붙일 수 있는 무대를 일찍 확보하십시오. '
    '둘째, 당신에게 맞지 않는 일은 아무리 조건이 좋아 보여도 과감히 덜어내는 용기가 필요합니다. '
    '셋째, 혼자 잘하는 것으로 끝내지 말고 반드시 당신의 결과물을 세상에 내놓는 출구 하나를 확보하십시오. '
    '이 셋이 맞물려 돌아갈 때 당신의 관상이 약속한 재능의 상한선이 열리기 시작합니다.',
  );

  return buf.toString();
}

