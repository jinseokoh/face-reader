      '오랜 세월이 빚어낸 깊이가 당신의 관상을 더 풍성하게 만들어주는 시기이며, 타고난 골격의 좋은 기운은 오히려 이 시기에 더 또렷하게 드러납니다. '
      '지금까지 해온 선택들이 얼굴에 그대로 기록되고 있다는 점을 자각할 수 있을 때, 앞으로의 선택이 달라지기 시작합니다. ',
    );
  } else if (r.ageGroup.isOver30) {
    buf.write(
      '당신의 현재 단계에서 관상이 강조하는 지점은 \'축적의 설계\'입니다. '
      '초기의 재능이 이미 드러난 시기이고, 지금부터 10년은 그 재능을 어떤 시스템 위에 올려놓느냐가 평생 곡선의 기울기를 결정합니다. '
      '당신의 관상에서 이 단계는 \'중년 발복\'의 기반이 만들어지는 구간이기에, 작은 선택들이 복리처럼 쌓여 5~7년 뒤 전혀 다른 풍경을 만들어냅니다. '
