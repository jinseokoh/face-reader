// 인생 질문 서술 — 사용자가 실제로 궁금해 하는 8개 영역의 본문.
// 각 섹션은 장점 → 단점 → 조언 3부 구성, 평균 750자 이상.
// 바람기는 20세 이상, 색기는 30세 이상에서만 노출.
// ═══════════════════════════════════════════════════════════════════════

String assembleLifeQuestions(FaceReadingReport r) {
  final parts = <MapEntry<String, String>>[
    MapEntry('타고난 재능', buildTalentNarrative(r)),
    MapEntry('재물운', buildWealthNarrative(r)),
    MapEntry('대인관계', buildSocialNarrative(r)),
    MapEntry('연애운', buildRomanceNarrative(r)),
  ];

  final philan = buildPhilanderingNarrative(r);
  if (philan != null) parts.add(MapEntry('바람기', philan));

  final sensual = buildSensualityNarrative(r);
  if (sensual != null) parts.add(MapEntry('색기', sensual));

  parts.add(MapEntry('건강과 수명', buildHealthNarrative(r)));
  parts.add(MapEntry('종합 조언', buildConclusionNarrative(r)));

  return parts.map((e) => '## ${e.key}\n${e.value}').join('\n\n');
}

// ─── Helpers ─────────────────────────────────────────────────────────────

double _attr(FaceReadingReport r, Attribute a) =>
    r.attributes[a]?.normalizedScore ?? 7.0;

bool _fired(FaceReadingReport r, String ruleId) =>
    r.rules.any((rule) => rule.id == ruleId);

double _nodeOwnZ(FaceReadingReport r, String nodeId) =>
    r.nodeScores[nodeId]?.ownMeanZ ?? 0.0;

double _nodeAbsZ(FaceReadingReport r, String nodeId) =>
    r.nodeScores[nodeId]?.ownMeanAbsZ ?? 0.0;

enum _Band { high, mid, low }

_Band _band(double s) {
  if (s >= 8.0) return _Band.high;
  if (s >= 6.5) return _Band.mid;
  return _Band.low;
}

bool _isMale(FaceReadingReport r) => r.gender == Gender.male;

