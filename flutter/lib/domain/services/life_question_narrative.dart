  final buf = StringBuffer();

  if (_band(stab) == _Band.high) {
    buf.write(
      '당신의 얼굴에는 건강의 뿌리가 깊게 박혀 있습니다. '
      '관상학에서 \'질액궁(疾厄宮)\'인 산근(山根)이 막힘 없이 열리고 턱(地閣)이 묵직하게 받쳐주는 상으로, '
