/// 친밀 섹션 pool — 30~50대 이성 gate 통과 페어 전용.
///
/// 구조 (옵션 2):
///   1. opener — intimacy.subScore bucket(high/mid/low)별 pool
///   2. axis detail — 4 axis × 3 sign × {cause, observation, adviceHigh,
///      adviceLow}. narrative 에서 bucket 에 따라 advice 를 선택 출력.
///   3. closing — bucket 별 pool
///
/// 톤 가이드: 에디토리얼 매거진 편집자 어투. 한자 어구·추상 표현·구시대
/// 도덕어 배제. low bucket 에서는 격려 대신 경계·관찰자 톤. high bucket
/// 에서만 적극 권유. 모든 axis 문단은 "어떤 얼굴이기에 이렇게 읽히는가"
/// 의 관상학적 근거로 시작해 농후함을 확보.
///
/// bucket 키: 'high'(≥65) · 'mid'(45~65) · 'low'(<65 이하). compat_narrative.dart
/// 의 `_intimacyBucket` 와 1:1 매칭. `{X}` 토큰은 narrative 쪽에서
/// subScore 정수값으로 치환.

/// axis × sign 한 조합의 3-part 서술.
class IntimacyAxisDetail {
  /// 관상학적 근거 — 어떤 얼굴 모양이기에 이렇게 읽히는가.
  final String cause;

  /// 실생활 관찰 — 이 얼굴 조합이 일상에서 어떻게 드러나는가.
  final String observation;

  /// high bucket 전용 조언 — 기회 인식·관리 권유.
  final String adviceHigh;

  /// low bucket 전용 조언 — 경계·속도 조절·자기 보호.
  final String adviceLow;

  const IntimacyAxisDetail({
    required this.cause,
    required this.observation,
    required this.adviceHigh,
    required this.adviceLow,
  });
}

